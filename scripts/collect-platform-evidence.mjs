import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, extname, join, resolve, sep } from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const args = parseArgs(process.argv.slice(2));
const platform = String(args.platform || process.env.NEDITOR_PLATFORM_EVIDENCE_PLATFORM || process.platform);
const evidenceDir = resolve(args["evidence-dir"] || process.env.NEDITOR_PLATFORM_EVIDENCE_DIR || join(root, ".tmp", "platform-evidence", "external"));
const bundleRoot = resolve(args["bundle-root"] || join(root, "src-tauri", "target", "release", "bundle"));
const webdriverReportPath = resolve(args["webdriver-report"] || join(root, ".tmp", "desktop-webdriver", "report.json"));
const buildCommand =
  args["build-command"] ||
  process.env.NEDITOR_PLATFORM_BUILD_COMMAND ||
  "pnpm run build && ./node_modules/.bin/tauri build --bundles all";

const platformSpecs = {
  win32: {
    name: "Windows",
    packagePath: "win32/package-artifacts.json",
    webdriverPath: "win32/tauri-webdriver-report.json",
    artifactKinds: new Map([
      [".msi", "msi"],
      [".exe", "nsis"],
    ]),
  },
  linux: {
    name: "Linux",
    packagePath: "linux/package-artifacts.json",
    webdriverPath: "linux/tauri-webdriver-report.json",
    artifactKinds: new Map([
      [".appimage", "appimage"],
      [".deb", "deb"],
      [".rpm", "rpm"],
    ]),
  },
};

const spec = platformSpecs[platform];
if (!spec) {
  console.error(
    `Platform evidence collection only supports Windows and Linux. Found ${platform}; run this on a supported host or pass --platform win32/linux for fixture validation.`,
  );
  process.exit(1);
}

const outputs = [];
if (!args["webdriver-only"]) {
  outputs.push(writePackageEvidence(spec));
}
if (!args["package-only"]) {
  outputs.push(writeWebdriverEvidence(spec));
}

console.log(`Collected ${spec.name} platform evidence:`);
for (const output of outputs) console.log(`- ${relative(output)}`);

function writePackageEvidence(spec) {
  const artifacts = collectPackageArtifacts(spec);
  if (artifacts.length === 0) {
    throw new Error(
      `No ${spec.name} package artifacts found under ${relative(bundleRoot)}. Run ${buildCommand} on the ${spec.name} host first.`,
    );
  }
  const reportPath = join(evidenceDir, spec.packagePath);
  mkdirSync(dirname(reportPath), { recursive: true });
  writeFileSync(
    reportPath,
    `${JSON.stringify(
      {
        schema: "neditor.platform-package-artifacts.v1",
        platform,
        status: "passed",
        generatedAt: new Date().toISOString(),
        command: buildCommand,
        artifacts,
        notes: `Generated by scripts/collect-platform-evidence.mjs on ${spec.name}.`,
      },
      null,
      2,
    )}\n`,
  );
  return reportPath;
}

function writeWebdriverEvidence(spec) {
  if (!existsSync(webdriverReportPath)) {
    throw new Error(`Desktop WebDriver report is missing: ${relative(webdriverReportPath)}. Run pnpm run test:tauri-webdriver first.`);
  }
  const report = JSON.parse(readFileSync(webdriverReportPath, "utf8"));
  if (report.platform !== platform) {
    throw new Error(`Desktop WebDriver report platform ${report.platform || "(missing)"} does not match ${platform}.`);
  }
  if (report.status !== "passed") {
    throw new Error(`Desktop WebDriver report status must be passed, found ${report.status || "(missing)"}.`);
  }
  const reportPath = join(evidenceDir, spec.webdriverPath);
  mkdirSync(dirname(reportPath), { recursive: true });
  writeFileSync(reportPath, `${JSON.stringify(report, null, 2)}\n`);
  return reportPath;
}

function collectPackageArtifacts(spec) {
  if (!existsSync(bundleRoot)) return [];
  return walk(bundleRoot)
    .map((path) => {
      const lower = path.toLowerCase();
      const extension = extname(lower);
      const kind = spec.artifactKinds.get(extension);
      if (!kind) return null;
      const normalizedKind = platform === "win32" && kind === "nsis" && !lower.split(sep).includes("nsis") ? "exe" : kind;
      const stats = statSync(path);
      if (!stats.isFile()) return null;
      return {
        kind: normalizedKind,
        path: relative(path),
        bytes: stats.size,
        sha256: sha256File(path),
      };
    })
    .filter(Boolean)
    .sort((a, b) => a.path.localeCompare(b.path));
}

function walk(directory) {
  const entries = [];
  for (const name of readdirSync(directory)) {
    const path = join(directory, name);
    const stats = statSync(path);
    if (stats.isDirectory()) {
      entries.push(...walk(path));
    } else {
      entries.push(path);
    }
  }
  return entries;
}

function sha256File(path) {
  return createHash("sha256").update(readFileSync(path)).digest("hex");
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = true;
    } else {
      parsed[key] = next;
      index += 1;
    }
  }
  return parsed;
}

function relative(path) {
  return path.startsWith(root) ? path.slice(root.length + 1) : path;
}
