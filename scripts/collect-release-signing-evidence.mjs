import { createHash } from "node:crypto";
import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const args = parseArgs(process.argv.slice(2));
const packageJson = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const platform = String(args.platform || process.env.NEDITOR_RELEASE_SIGNING_PLATFORM || process.platform);
const evidenceDir = resolve(args["evidence-dir"] || process.env.NEDITOR_RELEASE_SIGNING_DIR || join(root, ".tmp", "release-signing", "external"));
const releaseVersion = String(args["release-version"] || process.env.NEDITOR_RELEASE_VERSION || packageJson.version || "").trim();
const sourceCommit = String(args["source-commit"] || process.env.NEDITOR_SOURCE_COMMIT || gitCommit()).trim();

const signingSpecs = {
  darwin: {
    name: "macOS",
    evidencePath: "darwin/signing-evidence.json",
    requiredProof: ["codesign", "notarization", "spctl"],
    artifactKinds: ["app", "dmg"],
  },
  win32: {
    name: "Windows",
    evidencePath: "win32/signing-evidence.json",
    requiredProof: ["authenticode", "timestamp"],
    artifactKinds: ["msi", "nsis", "exe"],
  },
  linux: {
    name: "Linux",
    evidencePath: "linux/signing-evidence.json",
    requiredProof: ["package-signature", "checksum"],
    artifactKinds: ["appimage", "deb", "rpm"],
  },
};

const spec = signingSpecs[platform];
if (!spec) {
  console.error(`Release signing evidence collection supports darwin, win32, and linux. Found ${platform}.`);
  process.exit(1);
}
if (!releaseVersion) {
  console.error("Release version is required. Pass --release-version or set NEDITOR_RELEASE_VERSION.");
  process.exit(1);
}
if (releaseVersion !== packageJson.version) {
  console.error(`Release version must match package.json version ${packageJson.version}. Found ${releaseVersion}.`);
  process.exit(1);
}
if (!sourceCommit) {
  console.error("Source commit is required. Run from a Git checkout or pass --source-commit / NEDITOR_SOURCE_COMMIT.");
  process.exit(1);
}

const artifacts = collectArtifacts(spec, repeated(args.artifact));
const proof = collectProof(spec, repeated(args.proof));
const reportPath = join(evidenceDir, spec.evidencePath);
mkdirSync(dirname(reportPath), { recursive: true });
writeFileSync(
  reportPath,
  `${JSON.stringify(
    {
      schema: "neditor.release-signing-evidence.v1",
      platform,
      status: "passed",
      generatedAt: new Date().toISOString(),
      releaseVersion,
      sourceCommit,
      artifacts,
      proof,
      notes: `Generated by scripts/collect-release-signing-evidence.mjs on ${spec.name}.`,
    },
    null,
    2,
  )}\n`,
);

console.log(`Collected ${spec.name} release signing evidence: ${relative(reportPath)}`);

function collectArtifacts(spec, values) {
  if (values.length === 0) {
    throw new Error(
      `At least one signed ${spec.name} artifact is required. Pass --artifact kind=/path/to/artifact where kind is one of ${spec.artifactKinds.join(", ")}.`,
    );
  }
  return values.map((value) => {
    const [kind, path] = parsePair(value, "--artifact");
    const normalizedKind = kind.toLowerCase();
    if (!spec.artifactKinds.includes(normalizedKind)) {
      throw new Error(`Artifact kind ${kind} is not valid for ${spec.name}; expected ${spec.artifactKinds.join(", ")}.`);
    }
    const absolutePath = resolve(path);
    if (!existsSync(absolutePath)) {
      throw new Error(`Signed artifact does not exist: ${path}`);
    }
    const stats = statSync(absolutePath);
    if (!stats.isFile() && !stats.isDirectory()) {
      throw new Error(`Signed artifact must be a file or bundle directory: ${path}`);
    }
    return {
      kind: normalizedKind,
      path: relative(absolutePath),
      bytes: artifactBytes(absolutePath, stats),
      sha256: sha256Artifact(absolutePath, stats),
    };
  });
}

function collectProof(spec, values) {
  const proofByKind = new Map();
  for (const value of values) {
    const [kind, command] = parsePair(value, "--proof");
    if (!spec.requiredProof.includes(kind)) {
      throw new Error(`Proof kind ${kind} is not valid for ${spec.name}; expected ${spec.requiredProof.join(", ")}.`);
    }
    const result = spawnSync(command, {
      cwd: root,
      encoding: "utf8",
      shell: true,
      timeout: 120_000,
    });
    if (result.status !== 0) {
      const detail = firstSummary(`${result.stdout || ""}\n${result.stderr || ""}`) || `exit status ${result.status}`;
      throw new Error(`Release signing proof command failed for ${kind}: ${detail}`);
    }
    proofByKind.set(kind, {
      kind,
      status: "passed",
      command,
      summary: firstSummary(`${result.stdout || ""}\n${result.stderr || ""}`) || "Command exited with status 0.",
    });
  }
  const missing = spec.requiredProof.filter((kind) => !proofByKind.has(kind));
  if (missing.length > 0) {
    throw new Error(`Missing required ${spec.name} proof command(s): ${missing.join(", ")}. Pass --proof kind='command'.`);
  }
  return spec.requiredProof.map((kind) => proofByKind.get(kind));
}

function artifactBytes(path, stats) {
  if (stats.isFile()) return stats.size;
  return walkFiles(path).reduce((total, file) => total + statSync(file).size, 0);
}

function sha256Artifact(path, stats) {
  const hash = createHash("sha256");
  if (stats.isFile()) {
    hash.update(readFileSync(path));
  } else {
    for (const file of walkFiles(path).sort()) {
      hash.update(relative(file));
      hash.update("\0");
      hash.update(readFileSync(file));
      hash.update("\0");
    }
  }
  return hash.digest("hex");
}

function walkFiles(directory) {
  const entries = [];
  for (const entry of statSync(directory).isDirectory() ? readdirSync(directory) : []) {
    const path = join(directory, entry);
    const stats = statSync(path);
    if (stats.isDirectory()) entries.push(...walkFiles(path));
    else if (stats.isFile()) entries.push(path);
  }
  return entries;
}

function parsePair(value, label) {
  const index = String(value).indexOf("=");
  if (index <= 0 || index === String(value).length - 1) {
    throw new Error(`${label} must use kind=value syntax.`);
  }
  return [String(value).slice(0, index), String(value).slice(index + 1)];
}

function repeated(value) {
  if (value === undefined) return [];
  return Array.isArray(value) ? value : [value];
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const next = argv[index + 1];
    const value = !next || next.startsWith("--") ? true : next;
    if (parsed[key] === undefined) parsed[key] = value;
    else if (Array.isArray(parsed[key])) parsed[key].push(value);
    else parsed[key] = [parsed[key], value];
    if (value !== true) index += 1;
  }
  return parsed;
}

function firstSummary(text) {
  return text
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .slice(0, 3)
    .join(" | ")
    .slice(0, 500);
}

function relative(path) {
  return path.startsWith(root) ? path.slice(root.length + 1) : path;
}

function gitCommit() {
  const result = spawnSync("git", ["rev-parse", "HEAD"], {
    cwd: root,
    encoding: "utf8",
  });
  if (result.status !== 0) return "";
  return result.stdout.trim();
}
